package com.example.wj.android_per.common.eventbus;

public class UserBean {
    private String user_name;//用户名
    private String token;//用户名
    private String pass_word;//密码
    private String user_title;//手机号
    private String user_header;//头像
    private String user_grade;//等级
    private String user_memo;//描述
    private String user_sex;//性别
    private String user_address;

    public String getUser_name() {
        return user_name;
    }

    public String getToken() {
        return token;
    }

    public String getPass_word() {
        return pass_word;
    }

    public String getUser_title() {
        return user_title;
    }

    public String getUser_header() {
        return user_header;
    }

    public String getUser_grade() {
        return user_grade;
    }

    public String getUser_memo() {
        return user_memo;
    }

    public String getUser_sex() {
        return user_sex;
    }

    public String getUser_address() {
        return user_address;
    }
}
